export const keyUp = (event) => {
    switch (event.code) {
        case 'ArrowLeft':
        case 'KeyA':
            return 'left'
        case 'ArrowRight':
        case 'KeyD':
            return 'right'
    }
};

export const keyDown = (event) => {
    switch (event.code) {
        case 'ArrowLeft':
        case 'KeyA':
            return 'left'
        case 'ArrowRight':
        case 'KeyD':
            return 'right'
    }

};